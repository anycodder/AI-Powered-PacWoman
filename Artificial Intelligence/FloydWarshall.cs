    private void BuildGraphFromLabirent()
    {
        for (int i = 0; i < rowCount; i++)
        {
            for (int j = 0; j < columnCount; j++)
            {
                if (labirent[i, j] == 0) 
                {
                    int currentNodeIndex = i * columnCount + j;
                    
                    if (i > 0 && labirent[i - 1, j] == 0) 
                        AddEdge(currentNodeIndex, (i - 1) * columnCount + j, 1);
                    
                    if (j > 0 && labirent[i, j - 1] == 0) 
                        AddEdge(currentNodeIndex, i * columnCount + (j - 1), 1);
                    
                    if (i < rowCount - 1 && labirent[i + 1, j] == 0) 
                        AddEdge(currentNodeIndex, (i + 1) * columnCount + j, 1);
                    
                    if (j < columnCount - 1 && labirent[i, j + 1] == 0) 
                        AddEdge(currentNodeIndex, i * columnCount + (j + 1), 1);
                }
            }
        }
    }

    private void ComputePaths()
    {
        int nodeCount = rowCount * columnCount;
        for (int k = 0; k < nodeCount; k++)
        {
            for (int i = 0; i < nodeCount; i++)
            {
                for (int j = 0; j < nodeCount; j++)
                {
                    if (distances[i, k] < int.MaxValue / 2 && distances[k, j] < int.MaxValue / 2)
                    {
                        int potentialDistance = distances[i, k] + distances[k, j];
                        if (potentialDistance < distances[i, j])
                        {
                            distances[i, j] = potentialDistance;
                        }
                    }
                }
            }
        }
    }
    
    private void AddEdge(int from, int to, int weight)
    {
        if (from < 0 || from >= distances.GetLength(0) || to < 0 || to >= distances.GetLength(1))
            return; 
        distances[from, to] = weight;
        distances[to, from] = weight; 
        
    }

    public void SaveDistancesToFile(string filePath)
    {
        // Check if the file already exists
        if (!File.Exists(filePath))
        {
            int nodeCount = rowCount * columnCount;
            using (StreamWriter sw = new StreamWriter(filePath))
            {
                for (int i = 0; i < nodeCount; i++)
                {
                    sw.Write("{"); // Start of row
                    for (int j = 0; j < nodeCount; j++)
                    {
                        sw.Write(distances[i, j] + (j == nodeCount - 1 ? "" : ","));
                    }
                    sw.Write("}"); // End of row
                    sw.WriteLine(i == nodeCount - 1 ? "" : ","); // Add comma after each row except the last one
                }
            }
            Debug.Log("Distances saved to file: " + filePath);
        }
        else
        {
            Debug.Log("File already exists: " + filePath);
        }
    }


